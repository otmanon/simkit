def fcr_energy_F(F, mu, lam, vol):

    mu = mu.reshape(-1, 1)
    vol = vol.reshape(-1, 1)
    lam = lam.reshape(-1, 1)
    arap_e = 2 * arap_energy_F(F, mu,  vol);

    dim = F.shape[1]
    if dim == 2:
        I3 = np.linalg.det(F).reshape(-1, 1)
        E_vol = 0.5 * lam * vol * ((I3 - 1.0 )**2);
    elif dim == 3:
        I3 = np.linalg.det(F).reshape(-1, 1)
        E_vol = 0.5 * lam * vol * ((I3  - 1.0 )**2);
    
    E = np.sum(E_vol)[None] + arap_e
    return E



